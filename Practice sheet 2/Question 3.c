//Write a C program to check whether a number is negative, positive or zero.
#include <stdio.h>
int main(){  
    int number=3;
    if (number<0){
        printf("%d is negative",number);}
    else if (number>0)
    {
         printf("%d is positive",number);}
    else{
        printf("%d is zero",number);
    }
    return 0;
}




